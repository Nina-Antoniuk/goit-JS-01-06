  items: [],
  getItems() {
    return this.items;
  },
  add(product) {
    for (let i = 0; i < this.items.length; i += 1) {
      this.items[i].quantity = 1; //?
      if (this.items[i].name === product.name) {
        this.items[i].quantity += 1;
        return;
      }
    }
    this.items.push(product);
  },
  remove(productName) {
    for (let i = 0; i < this.items.length; i += 1) {
      if (this.items[i].name === productName) {
        this.items.splice(i, 1)
      }
    }
   },
